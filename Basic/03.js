// Arithmatic Operator
let x=10;
let y=3;
console.log(x+y); // add
console.log(x-y); // sub
console.log(x*y); // mul
console.log(x/y); // div - > quotient 
console.log(x%y); // div - > remainder
console.log(y**2);// exponent - > (3)^2


// Assingment Operator
let a=10;
a+=10;  // a=a+10 => 10+10 => 20
a/=2;   
a%=5;
a-=2;


// Relation Operator or Comparision Operator 
console.log(12>25); //  compare first operands_1 by operands_2 // return boolean


// Logiacal Operator
console.log(true && false);
console.log(true || false);    // or
console.log((9<9) && (9<9));   // and  
console.log(!(3>5));          //  not 
console.log(9<9)              // false or comparing equal value
